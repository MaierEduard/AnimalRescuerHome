package org.fasttrackit;

public class Cat extends Animal {

    public Cat(String name, int age, String favoritGame) {
        this.setHealthLevel(7);
        this.setHappynessLevel(1);
        this.setHungerLevel(6);
        this.setName(name);
        this.setAge(age);
        this.setFavoritActivityName(favoritGame);

    }
}



